// Search and filter functions for RAG
export function searchFurniture(query: string, filters?: {
  roomType?: string;
  style?: string;
  maxPrice?: number;
  category?: string;
}): FurnitureItem[] {
  const searchTerms = query.toLowerCase().split(' ').filter(term => term.length > 0);
  
  return furnitureDataset.filter(item => {
    // Text search - only if query is provided
    if (query.trim()) {
      const searchableText = [
        item.name,
        item.description,
        item.color,
        item.material,
        ...item.style_tags,
        ...item.space_type,
        ...item.room_style
      ].join(' ').toLowerCase();
      
      // Check if at least some terms match (not all)
      const matchingTerms = searchTerms.filter(term => 
        searchableText.includes(term)
      );
      
      // Require at least 2 matching terms or if query is short, at least 1
      const minMatches = searchTerms.length <= 3 ? 1 : Math.max(2, Math.floor(searchTerms.length * 0.3));
      if (matchingTerms.length < minMatches) return false;
    }
    
    // Apply filters
    if (filters?.roomType && !item.space_type.includes(filters.roomType)) {
      return false;
    }
    
    if (filters?.style && !item.room_style.includes(filters.style)) {
      return false;
    }
    
    if (filters?.maxPrice && item.price > filters.maxPrice) {
      return false;
    }
    
    if (filters?.category && item.category_id !== filters.category) {
      return false;
    }
    
    return true;
  });
}

export function getFurnitureByRoomType(roomType: string, style?: string, maxPrice?: number): FurnitureItem[] {
  return searchFurniture('', {
    roomType,
    style,
    maxPrice,
  });
}

export function getFurnitureByCategory(category: string, roomType?: string, style?: string): FurnitureItem[] {
  return searchFurniture('', {
    roomType,
    style,
    category,
  });
} 